int	ft_atoi(char *str)
{
	int	i;
	int 	min;
	int	num;

	min = 0;
	i = 0;
	while (!*str)
	{
		if ((str[i] == ' ') || ((str[i] >= 9) && (str[i] <=13)))
		{
			i++;
		}
		if (str[i] == '-')
		{
			min++;
		}
		if (min % 2 == 1)
		{
			num *= -1;
		}
		if ((str[i] >= '0') && (str[i] <= '9'))
		{
			return(num);
			i++;
		}
	}
	return (0);
}

